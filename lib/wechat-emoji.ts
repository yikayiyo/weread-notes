const emojiNames = Object.keys(WECHAT_EMOJI_MAP).sort(
  (a, b) => b.length - a.length,
);

const emoticonPattern = new RegExp(
  `\\[(${emojiNames.map((name) => name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")).join("|")})\\]`,
  "g",
);

export function replaceWechatEmoticons(text: string): string {
  return text.replace(
    emoticonPattern,
    (match, name: string) => WECHAT_EMOJI_MAP[name] ?? match,
  );
}
